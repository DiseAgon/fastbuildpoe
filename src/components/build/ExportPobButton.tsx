"use client";

import { useState } from "react";
import type { ItemSetView, ParsedItem } from "@/types/item";
import { useBuild, formatDivine } from "./BuildContext";

/** Build the plain-text price breakdown that gets written into PoB Notes. */
function buildNotes(
  view: ItemSetView,
  getPrice: (k: string) => string,
  keyFor: (i: ParsedItem) => string,
  sumItems: (items: ParsedItem[]) => number,
): string {
  const lines: string[] = [];
  const allGems = view.gems.flatMap((g) => g.gems);
  const groups: Array<[string, ParsedItem[]]> = [
    ["Gear", view.gear],
    ["Jewels", view.jewels],
    ["Gems", allGems],
    ["Flasks", view.flasks],
    ["Charms", view.charms],
  ];

  for (const [label, items] of groups) {
    const priced = items.filter((it) => {
      const v = Number.parseFloat(getPrice(keyFor(it)) || "");
      return Number.isFinite(v) && v > 0;
    });
    if (priced.length === 0) continue;
    lines.push(`[${label}] ${formatDivine(sumItems(items))} div`);
    for (const it of priced) {
      const price = getPrice(keyFor(it));
      const name = it.baseType && it.baseType !== it.name ? `${it.name} (${it.baseType})` : it.name;
      const slot = it.slot ? `${it.slot} — ` : "";
      lines.push(`  ${slot}${name}: ${price} div`);
    }
  }

  const total = sumItems([...view.gear, ...view.jewels, ...allGems, ...view.flasks, ...view.charms]);
  const header = `=== FastBuildPOE — Price Check ===\nTotal: ${formatDivine(total)} div\n`;
  return `${header}\n${lines.join("\n")}\n\n(generated by fastbuildpoe.xyz)`;
}

export function ExportPobButton({ view, input }: { view: ItemSetView; input: string }) {
  const { getPrice, keyFor, sumItems } = useBuild();
  const [code, setCode] = useState<string | null>(null);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const [copied, setCopied] = useState(false);

  if (!input) return null; // export needs the original PoB input

  async function handleExport() {
    setLoading(true);
    setError(null);
    setCopied(false);
    try {
      const notes = buildNotes(view, getPrice, keyFor, sumItems);
      const res = await fetch("/api/build/export", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ input, notes }),
      });
      const json = await res.json();
      if (json.success && json.data?.code) setCode(json.data.code as string);
      else setError(json.error ?? "Export failed.");
    } catch {
      setError("Could not reach the server.");
    } finally {
      setLoading(false);
    }
  }

  async function copy() {
    if (!code) return;
    try {
      await navigator.clipboard.writeText(code);
      setCopied(true);
      window.setTimeout(() => setCopied(false), 2000);
    } catch {
      /* ignore */
    }
  }

  return (
    <>
      <button
        type="button"
        onClick={handleExport}
        disabled={loading}
        className="rounded-[var(--radius)] border border-border bg-surface px-3 py-1 text-sm text-muted transition-colors hover:border-accent/50 hover:text-accent disabled:opacity-50"
      >
        {loading ? "Exporting…" : "Export to PoB ↗"}
      </button>

      {code && (
        <div
          className="fixed inset-0 z-50 flex items-center justify-center bg-black/60 p-4"
          onClick={() => setCode(null)}
        >
          <div
            className="flex w-full max-w-lg flex-col gap-3 rounded-[var(--radius)] border border-border bg-surface p-5"
            onClick={(e) => e.stopPropagation()}
          >
            <div className="flex items-center justify-between">
              <h3 className="font-serif text-lg text-accent">Path of Building code</h3>
              <button type="button" onClick={() => setCode(null)} className="text-muted hover:text-text">
                ✕
              </button>
            </div>
            <p className="text-sm text-muted">
              Prices are written into the build's Notes. In Path of Building: Import → paste this code.
            </p>
            <textarea
              readOnly
              value={code}
              onFocus={(e) => e.currentTarget.select()}
              className="h-28 w-full resize-none rounded-[6px] border border-border bg-bg px-2 py-1 font-mono text-xs text-text"
            />
            <button
              type="button"
              onClick={copy}
              className="self-start rounded-[var(--radius)] border border-accent/60 bg-accent/10 px-4 py-1.5 text-sm font-medium text-accent hover:bg-accent/20"
            >
              {copied ? "Copied ✓" : "Copy code"}
            </button>
          </div>
        </div>
      )}

      {error && <span className="text-xs text-red-400">{error}</span>}
    </>
  );
}
